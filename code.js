function sayHello(name) {
    if (name === undefined) {
        return 'Hello, World!';
    }
    return "Hello, " + name + "!";

    if (name === "Alex") {
        return "Hello, Alex!"
    }
    if (name === "Pat") {
        return "Hello, Pat!";
    }
    return "";
}

